import 'dart:async';

import 'package:flutter/foundation.dart';

/// «Токен», на который разбивается ответ при стриминге. Включает пробелы,
/// чтобы при склейке сохранялись переносы и отступы Markdown.
final RegExp _tokenSeparator = RegExp(r'(\s+)');

/// Контракт ИИ-сервиса.
///
/// Возвращает ответ стримом «токенов» (слов/кусков текста), чтобы интерфейс
/// мог показывать эффект печатания. Реальную реализацию (HTTP к OpenAI,
/// Anthropic, локальной модели и т.п.) можно подключить, не меняя UI.
abstract class AiService {
  Stream<String> send(String prompt);
}

/// Имитация ИИ для демонстрации интерфейса без сетевых запросов.
///
/// Подбирает один из заготовленных Markdown-ответов по ключевым словам и
/// «выдаёт» его по словам с задержкой, имитируя генерацию.
class MockAiService implements AiService {
  @visibleForTesting
  static const Duration thinkingDelay = Duration(milliseconds: 650);

  static const Duration _tokenDelay = Duration(milliseconds: 28);

  @override
  Stream<String> send(String prompt) async* {
    final response = _pickResponse(prompt);
    await Future<void>.delayed(thinkingDelay);
    for (final token in response.split(_tokenSeparator)) {
      await Future<void>.delayed(_tokenDelay);
      yield token;
    }
  }

  String _pickResponse(String prompt) {
    bool has(String pattern) =>
        RegExp(pattern, caseSensitive: false).hasMatch(prompt);

    if (has(r'привет|здравств|хай|hi|hello|добр(ое|ый)')) {
      return kGreeting;
    }
    if (has(r'код|программ|функц|алгоритм|python|dart|flutter|bug|ошибк')) {
      return kCodeAnswer;
    }
    if (has(r'иде[яю]|пет-проект|стартап|бизнес|приложение')) {
      return kIdeasAnswer;
    }
    if (has(r'стих|поэм|haiku|хайку|поэз')) {
      return kPoemAnswer;
    }
    if (has(r'квант|запутан|нейросет|машинн|искусствен')) {
      return kAiAnswer;
    }
    return kDefaultAnswer;
  }
}

